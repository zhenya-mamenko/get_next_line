char	*ft_strcat(char *dest, char *src)
{
	int i;
	int c;

	i = 0;
	c = 0;
	while (1)
	{
		if (c > 0 || !dest[i])
		{
			dest[i] = src[c];
			if (!src[c])
				break ;
			c += 1;
		}
		i += 1;
	}
	return (dest);
}
